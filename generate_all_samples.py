BD = Border(left=Side(style='thin'), right=Side(style='thin'),
            top=Side(style='thin'), bottom=Side(style='thin'))
FH = Font(name="微软雅黑", bold=True, size=10); FN = Font(name="微软雅黑", size=10)
FIH = PatternFill("solid", fgColor="D6E4F0")
AC = Alignment(horizontal="center"); AL = Alignment(horizontal="left")

def hdr(ws, row, headers):
    for c, h in enumerate(headers, 1):
        cell = ws.cell(row, c, h); cell.font = FH; cell.fill = FIH
        cell.alignment = AC; cell.border = BD

def dat(ws, row, vals, nfmt_cols=None):
    for c, v in enumerate(vals, 1):
        cell = ws.cell(row, c, v); cell.font = FN; cell.border = BD
        if nfmt_cols and c in nfmt_cols and isinstance(v, (int, float)):
            cell.number_format = '#,##0.00'

def set_widths(ws, widths):
    for c, w in widths: ws.column_dimensions[c].width = w

